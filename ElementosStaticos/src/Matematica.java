
public class Matematica {
	//criado um método estático
	//Static serve para nao precisar instanciar um objeto para acessar o metodo
	public static int somar(int a, int b) {
		//esse método retorna soma
		return a+b;
	}
	
	public static int subtrair(int a, int b) {
		//já esse retorna subtração
		return a-b;
	}
}

